void	ft_sort_integer_table(int *tab, int size)
{
	int i;
	int tmp;

	i = 1;
	while (i < size)
	{
		if (i < 1)
			i++;
		if (tab[i] < tab[i - 1])
		{
			tmp = tab[i];
			tab[i] = tab[i - 1];
			tab[i - 1] = tmp;
			i--;
		}
		else
			i++;
	}
}
